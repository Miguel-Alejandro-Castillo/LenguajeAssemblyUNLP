ORG 1000H
VOCALEZ DB "AEIOUaeiou"
        DB 0
CADENA  DB "PEPITA LA PISTOLERA"
        DB 0

ORG 3000H
ES_VOCAL: PUSH BX; GUARDAR POR SI LAS MOSCAS EL REGISTRO BX EN LA PILA
MOV CL, 00H
MOV BX, OFFSET VOCALEZ
LUPITA: CMP BYTE PTR [BX], 0
JZ FIN
CMP BYTE PTR [BX], AL
JZ ENCONTRE_VOCAL
INC BX
JMP LUPITA
ENCONTRE_VOCAL: MOV CL, 0FFH
FIN: POP BX
RET

ORG 4000H
VOCALES: MOV DL, 0
LUPE: CMP BYTE PTR [BX], 0
JZ FIN_1
MOV AL, [BX]
CALL ES_VOCAL
CMP CL, 0FFH
JNZ NO_CONTAR
INC DL
NO_CONTAR: INC BX
JMP LUPE
FIN_1: RET


ORG 2000H
MOV BX, OFFSET CADENA
CALL VOCALES
HLT
END

